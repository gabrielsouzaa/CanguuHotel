   public void gerarCliente() throws SQLException{
       
       Clientes cliente = new Clientes();
       cliente.setCpf(jTextField1_cpf.getText());
       cliente.setData_nascimento(jTextField1_dataNascimento.getText());
       cliente.setEmail(jTextField1_email.getText());
       cliente.setNome_completo(jTextField1_nomeCompleto.getText());
       cliente.setRg(jTextField1_rg.getText());
       cliente.setTelefone(jTextField1_telefone.getText());
        
       ClienteOperacoes operacoes = new ClienteOperacoes();
       operacoes.cadastrarCliente(cliente, this);
   }
  
