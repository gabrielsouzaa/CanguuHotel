    private void jButton2_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2_salvarActionPerformed
        ClienteOperacoes operacao = new ClienteOperacoes();
        Clientes cliente = new Clientes();
        cliente.setId(Integer.parseInt(txtID.getText()));
        cliente.setNome_completo(txtNome.getText());
        cliente.setCpf(txtCampoPesquisa.getText());
        cliente.setRg(txtRg.getText());
        cliente.setEmail(txtEmail.getText());
        cliente.setTelefone(txtTelefone.getText());
        cliente.setData_nascimento(txtNascimento.getText());
        operacao.editarCliente(cliente, this);
    }//GEN-LAST:event_jButton2_salvarActionPerformed

